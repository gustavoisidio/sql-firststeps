CREATE TABLE empregado(
	CPF NUMBER,
    	S_CPF NUMBER, 
    	END_CEP NUMBER,
	END_NUMERO NUMBER,
	SALARIO NUMBER,
	CONSTRAINT EMPREGADO_PK PRIMARY KEY (CPF), 
    	CONSTRAINT EMPREGADO_FK FOREIGN KEY (S_CPF) REFERENCES empregado(CPF)
);

CREATE TABLE telefones(
    CPF NUMBER,
    TELEFONE VARCHAR2(14),
    CONSTRAINT TELEFONES_PK PRIMARY KEY (CPF,TELEFONE),
    CONSTRAINT CPF_FK FOREIGN KEY (CPF) REFERENCES empregado(CPF) 
);



CREATE TABLE assento(
    ASSENTO VARCHAR2(10),
    CLASSE VARCHAR2 (20),
    CONSTRAINT ASSENTO_PK PRIMARY KEY(ASSENTO)
);

CREATE TABLE passagempassageiro(
    CPF NUMBER,
    DATA_PASSAGEM DATE,
    CPF_COMPRADOR NUMBER,
    ASSENTO VARCHAR2(10),
    CONSTRAINT PASSAGEMPASSAGEIRO_PK PRIMARY KEY(CPF,DATA_PASSAGEM),
    CONSTRAINT PASSAGEMPASSAGEIRO_FK FOREIGN KEY(ASSENTO) REFERENCES ASSENTO(ASSENTO)	
);

CREATE TABLE modelo(
	MODELO VARCHAR2 (20),
	CAPACIDADE NUMBER,
    CONSTRAINT MODELO_PK PRIMARY KEY(MODELO)	
);

CREATE TABLE aviao(
    NUMERO_DE_SERIE NUMBER,
    MODELO VARCHAR2 (20),
    CONSTRAINT AVIAO_PK PRIMARY KEY(NUMERO_DE_SERIE),
   CONSTRAINT  AVIAO_FK FOREIGN KEY (MODELO) REFERENCES MODELO  (MODELO)
);

CREATE TABLE copiloto(
	CPF NUMBER,
	CHT NUMBER,
    CONSTRAINT COPILOTO_PK PRIMARY KEY(CPF),
    CONSTRAINT COPILOTO_FK FOREIGN KEY(CPF) REFERENCES empregado(CPF)
);

CREATE TABLE piloto(
	CPF NUMBER,
	CHT NUMBER,
	CONSTRAINT PILOTO_PK PRIMARY KEY(CPF),
	CONSTRAINT PILOTO_FK FOREIGN KEY(CPF) REFERENCES empregado(CPF)
);

CREATE TABLE comissario(
	CPF NUMBER,
	CONSTRAINT COMISSARIO_FK FOREIGN KEY(CPF) REFERENCES empregado(CPF),
	CONSTRAINT COMISSARIO_PK PRIMARY KEY(CPF)
);

CREATE TABLE passageiro(
    CPF NUMBER,
    NOME VARCHAR2 (50),
    SEXO CHAR (1),
    CONSTRAINT CPF_PK PRIMARY KEY(CPF)
);

CREATE TABLE voa(
    NUMERO_DE_SERIE NUMBER,
    PASS_CPF NUMBER,
    PILOTO_CPF NUMBER,
    DATA_VOO DATE,
    CONSTRAINT VOA_PK PRIMARY KEY(NUMERO_DE_SERIE,PASS_CPF,PILOTO_CPF,DATA_VOO),
    CONSTRAINT VOA_FK FOREIGN KEY(NUMERO_DE_SERIE) REFERENCES aviao(NUMERO_DE_SERIE),
   	CONSTRAINT VOA_FK2 FOREIGN KEY(PASS_CPF) REFERENCES passageiro(CPF),
	CONSTRAINT VOA_FK3 FOREIGN KEY(PILOTO_CPF) REFERENCES piloto(CPF)
);

CREATE TABLE gratificacao(
	COD_GRATIFICACAO NUMBER,
	DATA_GRAT DATE,
	DESCRICAO VARCHAR2 (50),
	CONSTRAINT GRATIFICAO_PK PRIMARY KEY(COD_GRATIFICACAO) 
);

CREATE TABLE viagem(
    CODVIAGEM NUMBER,
    DESTINO_FINAL VARCHAR(50),
    PARTIDA_INICIAL VARCHAR(50),
    PRECO NUMBER,
    COMCPF NUMBER UNIQUE NOT NULL,
    CONSTRAINT VIAGEM_FK FOREIGN KEY(COMCPF) REFERENCES comissario(CPF),
    CONSTRAINT VIAGEM_PK PRIMARY KEY(CODVIAGEM)
);

CREATE TABLE compart(
	CODVIAGEM NUMBER,
	COMCPF NUMBER,
	CONSTRAINT COMPART_PK PRIMARY KEY(CODVIAGEM, COMCPF),
    CONSTRAINT COMPART_FK FOREIGN KEY(CODVIAGEM) REFERENCES viagem(CODVIAGEM),
    CONSTRAINT COMPART_FK2 FOREIGN KEY(COMCPF) REFERENCES comissario(CPF) 
);

CREATE TABLE recebe(
	COD_GRATIFICACAO NUMBER,
	CODVIAGEM NUMBER,
	COMCPF NUMBER,
	VALOR NUMBER,
	CONSTRAINT RECEBE_PK  PRIMARY KEY(COD_GRATIFICACAO, CODVIAGEM, COMCPF),
	CONSTRAINT RECEBE_FK  FOREIGN KEY(COD_GRATIFICACAO) REFERENCES gratificacao(COD_GRATIFICACAO),
	CONSTRAINT RECEBE_FK2 FOREIGN KEY(CODVIAGEM,COMCPF) REFERENCES compart(CODVIAGEM,COMCPF)
);

CREATE TABLE participa(
	CPFCOP NUMBER,
	CODVIAGEM NUMBER,
	CONSTRAINT PARTICIPA_PK PRIMARY KEY(CODVIAGEM,CPFCOP),
	CONSTRAINT PARTICIPA_FK FOREIGN KEY(CPFCOP) REFERENCES copiloto(CPF),
	CONSTRAINT PARTICIPA_FK2 FOREIGN KEY(CODVIAGEM) REFERENCES viagem(CODVIAGEM)
);


